/*
	prog will display use of DO while loops.
*/

//declaring class.
class Program
{
	//declaring main.
	static void Main(string[] args)
	{
		//declaring var.
		int counter = 1;
		
		//do loop counter while it is less than or = to 10.
		do
		{
			Console.WriteLine("This is loop number " + counter;
			//print above. 
			counter++;
			//incroment var counter by 1
		}
		while (counter <= 10)
		//end of while loop.
	
		Console.ReadKey();
		
	}//end of main. 
}//end of program class.