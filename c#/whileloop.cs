/*
	prog will display use of while loops.
*/

//declaring class.
class Program
{
	//declaring main.
	static void Main(string[] args)
	{
		//declaring var.
		int counter = 1;
		
		//loop counter var while it is less than or = to 10.
		while (counter <= 10)
		{
			Console.WriteLine("This is loop number " + counter;
			counter++;
		}//end of while loop.
		
		Console.ReadKey();
		
	}//end of main. 
}//end of program class.